import random

def flip_coin():
    """Returns 'Heads' or 'Tails' randomly."""
    choices = ['Heads', 'Tails']
    return random.choice(choices)

def roll_dice():
    """Returns a random number between 1 and 6."""
    return random.randint(1, 6)

def pick_student(student_list):
    """Picks a random student from a list for a pop quiz."""
    if not student_list:
        return "No students in the list!"
    return random.choice(student_list)


