## days_until_birthday(month, day): This function calculates the number of days remaining until a specified birthday. It takes month and day as arguments. If the birthday has already passed in the current year, it calculates the days until the birthday in the next year."""

from datetime import datetime, date

def get_todays_date():
    """Returns today's date as a nice string."""
    today = date.today()
    # Format: Monday, January 01, 2024
    return today.strftime("%A, %B %d, %Y")

def days_until_birthday(month, day):
    """Calculates days left until a birthday this year."""
    today = date.today()
    # Create a date object for the birthday this year
    birthday = date(today.year, month, day)

    # If birthday passed, look at next year
    if birthday < today:
        birthday = date(today.year + 1, month, day)

    delta = birthday - today
    return delta.days

